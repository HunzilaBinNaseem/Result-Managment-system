    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/db_invent","root","");
            String sql= "SELECT * from result  where ID = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1,Integer.parseInt(ID.getText()));

            ResultSet rs = ps.executeQuery();
            if(rs.next())
            {
                name.setText(rs.getString("Name"));
                rollno.setText(rs.getString("rollno"));
                stats.setText(rs.getString("s1"));
                oop.setText(rs.getString("s2"));
                eng.setText(rs.getString("s3"));
                arabic.setText(rs.getString("s4"));
                dld.setText(rs.getString("s5"));
                

            }
            else
            {

                JOptionPane.showMessageDialog(null,"RECORD NOT FOUND");
            }

        }catch(Exception e){
        }

    }//GEN-LAST:event_jButton4ActionPerformed

