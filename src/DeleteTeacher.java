                String dob = String.valueOf(rs.getString("T_DOB"));
                String department = String.valueOf(rs.getString("T_DEPARTMENT"));
                String username = String.valueOf(rs.getString("T_USERNAME"));
                String  password= String.valueOf(rs.getString("T_PASSWORD"));
