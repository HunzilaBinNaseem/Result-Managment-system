    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        // TODO add your handling code here:
        try{

            String query="INSERT INTO students (S_NAME,S_ADDRESS,S_PHONE,S_GENDER,S_DOB,S_SECTION,S_Username,S_Password,STUDENT_ID) VALUES(?,?,?,?,?,?,?,?,?)";

            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/db_invent","root","");

            pst=con.prepareStatement(query);
            pst.setString(1, studentname.getText());
            pst.setString(2,address1.getText());
            pst.setString(3,phone.getText());
            pst.setString(4, gender.getSelectedItem().toString());
            pst.setString(5, dob.getText());
            pst.setString(6, section.getText());
            pst.setString(7, username.getText());
            pst.setString(8, password.getText());
            pst.setString(9, studentid.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "REGISTRATION SUCCESSFUL");

        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);

        }
    }//GEN-LAST:event_registerActionPerformed

