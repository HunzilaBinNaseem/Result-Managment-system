    public DefaultTableModel getData() {
        DefaultTableModel dtm=new DefaultTableModel();
        dtm.addColumn("ID");
        dtm.addColumn("Name");
        dtm.addColumn("Phone");
        dtm.addColumn("Gender");
        dtm.addColumn("Address");
        dtm.addColumn("DOB");
        dtm.addColumn("Section");
        dtm.addColumn("Username");
        dtm.addColumn("Password");
        dtm.addColumn("Student ID");
        
        String sql="SELECT * FROM students";
        
        try{
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/db_invent","root","");
            
            Statement st=con.prepareStatement(sql);
            ResultSet rs=st.executeQuery(sql);
            
             while (rs.next()) {
                String name = rs.getString(1);
                String address = rs.getString(2);
                String phone = rs.getString(3);
                String gender = rs.getString(4);
                String dob = rs.getString(4);
                String section = rs.getString(5);
                String username = rs.getString(6);
                String  password= rs.getString(7);
                String  id= rs.getString(8);
                String  stdid= rs.getString(9);
                
                dtm.addRow(new String[]{name,address,phone,gender,dob,section,username,password,id,stdid});
            
        }
             return dtm;
        }catch (Exception ex) {
            ex.printStackTrace();
           
            }
        return null;
       
    }
    
