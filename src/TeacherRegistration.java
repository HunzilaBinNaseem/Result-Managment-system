   String query="INSERT INTO teachers (T_NAME,T_ADDRESS,T_PHONE,T_GENDER,T_DOB,T_DEPARTMENT,T_Username,T_Password) VALUES(?,?,?,?,?,?,?,?)";
   
        con=DriverManager.getConnection("jdbc:mysql://localhost:3306/db_invent","root","");
        
        pst=con.prepareStatement(query);
        pst.setString(1, teachername.getText());
        pst.setString(2,address.getText());
         pst.setString(3,phone.getText());
           pst.setString(4, gender.getSelectedItem().toString());
        pst.setString(5, dob.getText());
          pst.setString(6, department.getText()); 
        pst.setString(7, username.getText());
       pst.setString(8, password.getText());

        pst.executeUpdate();
